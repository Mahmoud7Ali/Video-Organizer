    private void Search_PlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_PlayerActionPerformed
        // TODO add your handling code here:
        String PlayerN = Search_Player_Field.getText().toString();
        
        DefaultTableModel VID_Table = (DefaultTableModel) Videos_Table.getModel();
        VID_Table.setRowCount(0);
        
        GLinkedList.GLinkedList.Node c = Link.LS.head;
        
        while(c != null)
        {
            if(action_Choos.getSelectedItem().toString() != null)
            {
                if(action_Choos.getSelectedItem().toString().compareTo("Select Category") != 0)
                {
                    if(c.PlayerName.compareTo(PlayerN) == 0 && action_Choos.getSelectedItem().toString().compareTo(c.Action) == 0)
                    {
                        String[] row = { c.VidName, c.Path, c.Action, c.PlayerName, c.Team1, c.Team2};
                        VID_Table.addRow(row);
                    }
                }
                else{
                    if(c.PlayerName.compareTo(PlayerN) == 0)
                    {
                        String[] row = { c.VidName, c.Path, c.Action, c.PlayerName, c.Team1, c.Team2};
                        VID_Table.addRow(row);
                    }
                }
            }
            else{
                if(c.PlayerName.compareTo(PlayerN) == 0)
                {
                    String[] row = { c.VidName, c.Path, c.Action, c.PlayerName, c.Team1, c.Team2};
                    VID_Table.addRow(row);
                }
            }
            
            c = c.next;
        }
    }//GEN-LAST:event_Search_PlayerActionPerformed

