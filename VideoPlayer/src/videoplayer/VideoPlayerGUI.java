    void get_data(){
            try {
                //call jdbc
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                //create connection
                Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=videos;user=mahmoudd;password=123456");
                
                Statement stm = con.createStatement();
                ResultSet rs = stm.executeQuery("SELECT * FROM Video");
                
                while(rs.next())
                {
                    String VN = rs.getString("VN");
                    String P = rs.getString("P");
                    String A = rs.getString("A");
                    String PL = rs.getString("PL");
                    String T1 = rs.getString("T1");
                    String T2 = rs.getString("T2");
                    
                    Link.LS.add(VN, P, A, PL, T1, T2);
                    //Linkedlist.ls.add(new Video(VN,P,A,PL,T1,T2));
                    String VideosData[] = {VN, P, A, PL, T1, T2};
                    DefaultTableModel VID_Table = (DefaultTableModel) Videos_Table.getModel();
                    VID_Table.addRow(VideosData);
                }
                //disconnect with database
                con.close();
            }catch (ClassNotFoundException ex) {
                ex.printStackTrace();
            }
            catch (Exception ex) {
                ex.printStackTrace();
            }
    }
